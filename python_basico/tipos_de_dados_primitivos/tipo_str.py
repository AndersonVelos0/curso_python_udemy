# Python é uma lingagem dinamica e fortemente tipada de forma dinamica, 
# Já sabe o tipo de dado passado na variavel.

# String

# Strings são textos que estão dentro de aspas, duplas ou simples

# Aspas Duplas
print("Olá, isso é uma string!") 

# Aspas Simples
print('Agora utilizando aspas simples') 

# Caracter de Escape \
print("Anderson \"Veloso\"")

# Mostrando o caracter de barra 
print(r"Luiz \"Veloso\"")

# Forma mais facil e avançada utilizando as aspas
print('Anderson "Veloso" ')
print("Anderson 'Veloso' ")
print('Explícito', 'é', 'melhor " do que implícito')




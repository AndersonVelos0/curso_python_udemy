# Adição
adicao = 10 + 10
print('Adição', adicao)

# Subtração
subtracao = 10 - 5
print('Subtração', subtracao)

# Multiplicação
multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

# Divisão
divisao = 10 / 3  # float
print('Divisão', divisao)

# Divisão Inteira
divisao_inteira = 10 // 3
print('Divisão inteira', divisao_inteira)

# Exponenciação (elevado a)
exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

# Módulo (resto)
modulo = 55 % 2  # resto da divisão
print('Módulo', modulo)
print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)